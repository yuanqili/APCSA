package lecture1;

public class StringOperations
{
    public static void main(String[] args)
    {
        String str1 = "Hi, " + "Bob";
        String str2 = "1" + "2" + "1";
        String str3 = "1234" + " + " + "99";
        String str4 = "1234" + "99";

        System.out.println(str1);
        System.out.println(str2);
        System.out.println(str3);
        System.out.println(str4);
    }
}
